int	ft_atoi(char *str)
{
	int		m;
	long	res;

	m = 0;
	res = 0;
	while (*str == ' ' || (*str > 8 && *str < 14))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			m++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + *str - '0';
		str++;
	}
	if (m % 2 != 0)
		res = -res;
	return (res);
}
