void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int l;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		l = tab[i];
		tab[i] = tab[j];
		tab[j] = l;
		i++;
		j--;
	}
}
